TRIGGER_KINDS = {cap.kind for cap in CATALOG if cap.is_trigger}
TRIGGER_NODE_TYPES = {cap.node_type for cap in CATALOG if cap.is_trigger}


def native_kinds() -> list[str]:
    return [cap.kind for cap in CATALOG]


def runtime_node_types() -> list[str]:
    return [cap.node_type for cap in CATALOG]


def get_capability_for_kind(kind: str) -> NodeCapability | None:
    return CAPABILITY_BY_KIND.get(kind)


def get_capability_for_node_type(node_type: str) -> NodeCapability | None:
    return CAPABILITY_BY_NODE_TYPE.get(node_type)


def describe_kind(kind: str) -> dict[str, object] | None:
    capability = get_capability_for_kind(kind)
    if capability is None:
        return None
    return {
        "kind": capability.kind,
        "runtime_node_type": capability.node_type,
        "purpose": describe_kind_purpose(capability.kind),
        "is_trigger": capability.is_trigger,
        "fields": [
            {
                "name": field.dsl_name,
                "runtime_key": field.export_path,
                "required": field.required,
                "description": describe_field(field),
                "ai_prompt": describe_field_for_ai(field),
                "example": example_for_field(field.dsl_name),
            }
            for field in capability.fields
        ],
        "allowed_fields": capability.field_names(),
        "required_fields": capability.required_fields(),
        "route_handles": list(capability.route_handles),
        "button_target_edges": capability.button_target_edges,
        "selected_message_target": capability.selected_message_target,
        "output_routes": capability.output_routes,
        "aliases": list(capability.aliases),
        "notes": list(capability.notes),
    }


