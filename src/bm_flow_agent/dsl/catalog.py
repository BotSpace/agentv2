from __future__ import annotations

from dataclasses import dataclass


@dataclass(frozen=True)
class FieldCapability:
    dsl_name: str
    export_path: str
    import_paths: tuple[str, ...] = ()
    required: bool = False
    description: str | None = None

    def import_candidates(self) -> tuple[str, ...]:
        return self.import_paths or (self.export_path,)


@dataclass(frozen=True)
class NodeCapability:
    kind: str
    node_type: str
    fields: tuple[FieldCapability, ...] = ()
    is_trigger: bool = False
    button_target_edges: bool = False
    selected_message_target: bool = False
    output_routes: bool = False
    route_handles: tuple[str, ...] = ()
    notes: tuple[str, ...] = ()
    aliases: tuple[str, ...] = ()

    def field_names(self) -> list[str]:
        return [field.dsl_name for field in self.fields]

    def required_fields(self) -> list[str]:
        return [field.dsl_name for field in self.fields if field.required]


def field(
    dsl_name: str,
    export_path: str,
    *import_paths: str,
    required: bool = False,
    description: str | None = None,
) -> FieldCapability:
    return FieldCapability(
        dsl_name=dsl_name,
        export_path=export_path,
        import_paths=tuple(import_paths),
        required=required,
        description=description,
    )


def capability(
    kind: str,
    node_type: str,
    *fields: FieldCapability,
    is_trigger: bool = False,
    button_target_edges: bool = False,
    selected_message_target: bool = False,
    output_routes: bool = False,
    route_handles: tuple[str, ...] = (),
    notes: tuple[str, ...] = (),
    aliases: tuple[str, ...] = (),
) -> NodeCapability:
    return NodeCapability(
        kind=kind,
        node_type=node_type,
        fields=fields,
        is_trigger=is_trigger,
        button_target_edges=button_target_edges,
        selected_message_target=selected_message_target,
        output_routes=output_routes,
        route_handles=route_handles,
        notes=notes,
        aliases=aliases,
    )


